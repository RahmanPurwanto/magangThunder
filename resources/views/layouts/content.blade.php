      <div class="entry">
        <p><strong>Thunderlabs indonesia</strong> merupakan divisi turunan dari
           <a href="https://www.loker.id/profile/vernon-office">Vernon</a>,
          begitu juga dengan  <a href="https://halomalang.com/">
